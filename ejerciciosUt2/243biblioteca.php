<?php
declare(strict_types()=1)
function suma(int $num1, int $num2):int
{
return $num1+$num2;
}

function resta(int $num1, int $num2):int
{
return $num1-$num2;
}

function multiplicacion(int $num1, int $num2):int
{
return $num1*$num2;
}

function division(int $num1, int $num2):int
{
return $num1/$num2;
}
?>